public class ExpandedBall {
    private int x;
    private int y;
    private int r1;
    private int r2;
    public ExpandedBall(int X, int Y, int radius1, int radius2){
        x = X;
        y = Y;
        r1 = radius1;
        r2 = radius2;
    }
}
